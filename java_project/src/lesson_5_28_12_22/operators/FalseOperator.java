package lesson_5_28_12_22.operators;

public class FalseOperator {
    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        System.out.println("!a(true) = " + !a); //!a(true) -> false
        System.out.println("!b(false) = " + !b); //!b(false) -> true

    }
}
